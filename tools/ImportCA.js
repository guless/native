var shell = require("shelljs");

/// 导入证书到 MAC 系统中。
if ( !shell.which("security") ) {
    console.log("Import CA with \"certtool\" command tools.");
    /// $: /usr/bin/certtool i "$cert" v k=/System/Library/Keychains/X509Anchors
    shell.exec("/usr/bin/certtool i \"./cert/cert.pem\" v k=/System/Library/Keychains/X509Anchors");
}

else {
    console.log("Import CA with \"security\" command tools.");
    /// $: /usr/bin/security add-trusted-cert -d -r trustRoot -k /Library/Keychains/System.keychain "$cert"
    shell.exec("/usr/bin/security add-trusted-cert -d -r trustRoot -k /Library/Keychains/System.keychain \"./cert/cert.pem\"");
}